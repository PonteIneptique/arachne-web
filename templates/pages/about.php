<h2 class="background-like">The source of the problem</h2>
<div class="shadow-content ">
	<p>Understanding the sense of a word is a complicated task, especially in ancient languages in which the meaning of some words has been lost over the centuries and only a vague remaining sense is still known. Some of these words, such as lasciuus, a, um, appear to have had their meanings twisted in french translations (« enjoué, gai, pétulant, etc. ») as well as in english translations (petulant, sportive, playful in Lewis & Short on Perseus.tufts.edu). But its large use and context in Martial's Epigrammata drove us to think that its meaning has, after centuries of -perhaps- too much decency and appropriateness in translations, faded away. This is why we decided to create a way to collect more information about it and its companion in the lexical field of sexuality.</p>
	<p>For this purpose, we designed Lasciva Roma. It will not only give us informations about <b>lascivus, a, um</b> but also on the semantic field of Sexuality and its context. It is by far the largest collection of data around this topic, driven by you.</p>
</div>

<h2 class="background-like">About the Clotho Project</h2>
<div class="shadow-content ">
	<p>Lasciva Roma is built on the Clotho project.</p>
	<p>
		The Clotho project is divided in two tools. The first one is based on python software,
		which enables the distant reading of the corpus and the export of its results. The other is a PHP
		platform which enables a team or a crowd to annotate and clean the results. Both are relatively
		easy to install and fully open source so new functionalities of wide application can be added.
	</p>
	<ul>
		<li><a href="https://github.com/PonteIneptique/clotho-local">Clotho Local (Python)</a>
			<p>Clotho Local will help you to find co-occurences, informations about context by using a nice command-line interface</p>
		</li>
		<li><a href="https://github.com/PonteIneptique/clotho-web">Clotho Web (Php)</a>
			<p>Clotho Web will help you to process manually the results of Clotho Local</p>
		</li>
	</ul>
</div>

